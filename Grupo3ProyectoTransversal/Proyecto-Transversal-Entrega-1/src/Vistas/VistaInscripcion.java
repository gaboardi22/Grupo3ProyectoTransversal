    private void jBInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBInscribirActionPerformed
        int filaSeleccionada = jTMaterias.getSelectedRow();
        if (filaSeleccionada != -1) {
            Alumno alumno = (Alumno) jCBAlumnos.getSelectedItem();
            int idMateria = (Integer) modelo.getValueAt(filaSeleccionada, 0);
            String nombreMateria = (String) modelo.getValueAt(filaSeleccionada, 1);
            int anioMateria = (Integer) modelo.getValueAt(filaSeleccionada, 2);
            Materia materia = new Materia(idMateria, nombreMateria, anioMateria, true);
            Inscripcion insc = new Inscripcion(alumno, materia, 0);
            inscData.guardarInscripcion(insc);
            borrarFilaTabla();
        }
    }//GEN-LAST:event_jBInscribirActionPerformed

    private void jBAnularInscripcionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAnularInscripcionActionPerformed
        int filaSeleccionada = jTMaterias.getSelectedRow();
        Alumno alumno = (Alumno) jCBAlumnos.getSelectedItem();
        if (filaSeleccionada != -1) {
            int idMateria=(Integer) modelo.getValueAt(filaSeleccionada, 0);
            inscData.borrarInscripcion(alumno.getIdAlumno(), idMateria);
            borrarFilaTabla();
        }
    }//GEN-LAST:event_jBAnularInscripcionActionPerformed


