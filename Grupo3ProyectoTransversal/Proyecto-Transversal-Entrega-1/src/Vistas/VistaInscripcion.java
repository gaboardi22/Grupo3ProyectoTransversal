    private void cargarAlumnos() {
        for (Alumno alumno : listaA) {
            jCBAlumnos.addItem(alumno);
        }
    }

    private void armarCabeceraTabla() {
        ArrayList<Object> filaCabecera = new ArrayList<>();
        filaCabecera.add("ID");
        filaCabecera.add("Nombre");
        filaCabecera.add("Año");
        for (Object object : filaCabecera) {
            modelo.addColumn(object);
        }
        jTMaterias.setModel(modelo);
    }

    private void borrarFilaTabla() {
        int indice = modelo.getRowCount() - 1;
        for (int i = indice; i >= 0; i--) {
            modelo.removeRow(i);
        }
    }

    private void cargaDatosNoIncriptos() {
        //this.borrarFilaTabla();
        Alumno selec = (Alumno) jCBAlumnos.getSelectedItem();
        listaM = (ArrayList) inscData.obtenerMateriasNoCursadasPorAlumno(selec.getIdAlumno());
        for (Materia materia : listaM) {
            modelo.addRow(new Object[]{materia.getIdMateria(), materia.getNombre(), materia.getAnio()});
        }
    }

    private void cargaDatosIncriptos() {
        //this.borrarFilaTabla();
        Alumno selec = (Alumno) jCBAlumnos.getSelectedItem();
        listaM = (ArrayList) inscData.obtenerMateriasAlumno(selec.getIdAlumno());
        for (Materia materia : listaM) {
            modelo.addRow(new Object[]{materia.getIdMateria(), materia.getNombre(), materia.getAnio()});
        }
    }
